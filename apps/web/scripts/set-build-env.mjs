// Cross-platform pre-build helper.
//
// Reads the current short git SHA and writes a .env.production.local-flavoured
// file that Next.js picks up for the upcoming build. Cross-platform (Node) so
// it works on both Linux/macOS dev machines and Coolify's build container.
//
// Why a file (vs an inline shell expansion): on Windows, `$(git rev-parse …)`
// in a package.json script string fails under cmd.exe. Generating a file is
// shell-agnostic.
//
// Output file: apps/web/.env.production.local (gitignored).

import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

let sha = '';
try {
  sha = execSync('git rev-parse --short HEAD', {
    cwd: join(__dirname, '..', '..', '..'),
    stdio: ['ignore', 'pipe', 'ignore'],
  })
    .toString()
    .trim();
} catch {
  // Not a git working tree, or git not available — emit empty SHA.
  sha = '';
}

const target = join(__dirname, '..', '.env.production.local');
const contents = `# Auto-generated by scripts/set-build-env.mjs — do not edit by hand.\nGIT_SHA=${sha}\n`;
writeFileSync(target, contents, 'utf8');

// eslint-disable-next-line no-console
console.log(`[set-build-env] wrote GIT_SHA=${sha || '(empty)'} to .env.production.local`);
